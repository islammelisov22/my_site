from django.core.validators import MaxValueValidator
from django.db import models


class Kit(models.Model):
    total_before = models.CharField(max_length=100)
    total_after = models.CharField(max_length=100)
    items = models.ManyToManyField(max_length=250, verbose_name='Введите товар', help_text='Товар')
    percent = models.CharField(validators=[MaxValueValidator])


def __str__(self):
    return self.title

